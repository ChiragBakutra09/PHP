<h3>Using while loop:</h3>
<?php
$i = 15;
while ($i <= 20) {
    echo $i . "<br>";
    $i++;
}
?>

<h3>Using do-while loop:</h3>
<?php
$j = 15;
do {
    echo $j . "<br>";
    $j++;
} while ($j <= 20);
?>

